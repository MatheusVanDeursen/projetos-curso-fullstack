    private void BtnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnProcurarActionPerformed
        try{
            
            String op="";
            for (Enumeration<AbstractButton> buttons = BtnGroup.getElements(); buttons.hasMoreElements();) {
                AbstractButton button = buttons.nextElement();
                if (button.isSelected()) {
                        op = button.getText();
                }
            }
            if(op.equals("")){
                return;
            }       

            ArrayList<Carro> carros = new ArrayList();
            if(op.equals("Modelo")){
                CarroController.getCarrosPorX(carros,"modelo",TextBusca.getText());
            }

            if(op.equals("Fabricante")){
                CarroController.getCarrosPorX(carros,"fabricante",TextBusca.getText());
            }

            if(op.equals("Cor")){
                System.out.println(TextBusca.getText());
                CarroController.getCarrosPorX(carros,"cor",TextBusca.getText());
                System.out.println(carros.get(0));
            }

            if(op.equals("Ano")){
                CarroController.getCarrosPorX(carros,"ano",TextBusca.getText());
            }

            if(op.equals("Preço")){
                CarroController.getCarrosPorX(carros,"aluguel",TextBusca.getText());
            }

            if(TextBusca.getText().equals("")){
                CarroController.getAll(carros);
            }
            
            ArrayList<Carro> aux = CarroController.OrdenaCarros(carros);
             
            this.carros = aux;
            
            System.out.println(this.carros.size());
            
            DefaultListModel model = new DefaultListModel(); 
            this.TextLista.setModel(model);         
            for(int i = 0; i <= this.carros.size(); i++){
                model.addElement(this.carros.get(i).getModelo());
            }
            
        }catch(NullPointerException e){
            
        }catch(IndexOutOfBoundsException e){
            
        }
    }//GEN-LAST:event_BtnProcurarActionPerformed

    

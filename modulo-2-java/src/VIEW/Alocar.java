    private void TextDInicialKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TextDInicialKeyTyped
        dias = 1;
        try{
            Date temp;     
            Calendar data_inicial = Calendar.getInstance();
            Calendar data_final = Calendar.getInstance();
            
            SimpleDateFormat sdftraco = new SimpleDateFormat("dd-MM-yyyy");
            SimpleDateFormat sdfbarra = new SimpleDateFormat("dd/MM/yyyy");

            if(TextDInicial.getText().contains("-")){
                temp = sdftraco.parse(TextDInicial.getText());
                Dinicial = temp;
                data_inicial.setTime(temp);
            }else{
                if(TextDInicial.getText().contains("/")){
                    temp = sdfbarra.parse(TextDInicial.getText());
                    Dinicial = temp;
                    data_inicial.setTime(temp);
                }else{
                    return;
                }
            }
            
            if(TextDFinal.getText().contains("-")){
                temp = sdftraco.parse(TextDFinal.getText());
                Dfinal = temp;
                data_final.setTime(temp);
            }else{            
                if(TextDFinal.getText().contains("/")){
                    temp = sdfbarra.parse(TextDFinal.getText());
                    Dfinal = temp;
                    data_inicial.setTime(temp);
                }else{
                    return;
                }
            }
            
            int MILLIS_IN_DAY = 86400000;

            data_inicial.set(Calendar.MILLISECOND, 0);
            data_inicial.set(Calendar.SECOND, 0);
            data_inicial.set(Calendar.MINUTE, 0);
            data_inicial.set(Calendar.HOUR_OF_DAY, 0);

            data_final.set(Calendar.MILLISECOND, 0);
            data_final.set(Calendar.SECOND, 0);
            data_final.set(Calendar.MINUTE, 0);
            data_final.set(Calendar.HOUR_OF_DAY, 0);
            Long tempo = ((data_final.getTimeInMillis() - data_inicial.getTimeInMillis()) / MILLIS_IN_DAY);
            
            dias += tempo.intValue();     
            
            System.out.println(dias);
            if(!TextDInicial.getText().matches("[a-zA-Z]+") && !TextDFinal.getText().matches("[a-zA-Z]+")){
                if(TextDInicial.getText().length()==10 && TextDFinal.getText().length()==10){
                    if(dias>0){                   
                        TextPrecoTotal.setText("" + Integer.parseInt(TextPreco.getText()) * dias);
                        return;
                    }
                }
            }
            
            TextPrecoTotal.setText("");
            
        } catch(Exception e){

        }

    }//GEN-LAST:event_TextDInicialKeyTyped

